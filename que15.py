s = "Hello everyone how are you"
print(s.split())  # ['Hello', 'everyone', 'how', 'are', 'you']

s = "Hello-everyone-how are you"
print(s.split("-"))  # ['Hello', 'everyone', 'how are you']

word = "pawani mittal :mathura"
print(word.split(":"))

t = "23456"
print(t.split())  # ['23456']
